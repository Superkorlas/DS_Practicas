    @Override
    public void run() {
        while (true) {
            double state = this.observable.getState();
            String temp = Double.toString(state);
            this.temperatura.setText(temp + "ยบ");
            
            double stateF = (state * 9 / 5) + 32;
            String tempF = Double.toString(stateF);
            this.temperaturaF.setText(tempF + "ยบF");
            
            
            System.out.println("Temperatura desde pantalla: " + temp + "ยบ");
            try {
                long interval = (long) Math.random() * 4 +1;
                Thread.sleep( interval * 5000);
            } catch (InterruptedException ex) {
                Logger.getLogger(Temperatura.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }    
